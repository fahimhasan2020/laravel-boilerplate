    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if (Sentinel::findByCredentials(['email' => $request->email])) {
            return response()->json(['error' => 'Email already exists'],500);
        }
        $user = Sentinel::registerAndActivate($request->all());
        $role = Sentinel::findRoleBySlug($request->role);
        $result = $role->users()->attach($user);
        if ($result)
            return response()->json(['success'=>'User created']);
        else
            return response()->json(['error'=>'User creation failed']);
    }

